"""
Check which Gutenberg excerpts have ≥1 word-level 13-gram overlap with the
FineWeb-Edu 160B training corpus.

Reads sampled.jsonl (must have text_excerpt field), reads the list of actually-tokenized
parquet filenames from --parquets-file (generated by parse_tokenized_parquets.py from
DataTrove task logs), scans each parquet for matches, writes output JSONL with added
in_fineweb and fineweb_matching_ngrams.

Usage:
    python -m attn_bench.data_processing.books.check_fineweb_containment \\
        --sampled     /path/to/sampled.jsonl \\
        --completed-dumps-dir /path/to/completed_dumps \\
        --raw-dir     /path/to/fineweb-edu-dedup \\
        --output      /path/to/sampled_containment.jsonl \\
        --stats-dir   /path/to/stats/fineweb_containment \\
        --workers     96
"""

from __future__ import annotations

import argparse
import json
import time
from multiprocessing import Pool
from pathlib import Path

import numpy as np
import xxhash
import pyarrow.parquet as pq

NGRAM_SIZE = 13

# Populated in main() before Pool is created; inherited copy-on-write by all workers.
_HASH_TO_BOOKS: dict[int, list[int]] = {}


def _ngram_hashes(text: str) -> list[int]:
    words = text.lower().split()
    return [
        xxhash.xxh64(' '.join(words[i:i + NGRAM_SIZE])).intdigest()
        for i in range(len(words) - NGRAM_SIZE + 1)
    ]


def _scan_parquet(parquet_path: str) -> dict[int, int]:
    """Return {excerpt_idx: matching_ngram_count} for one parquet file."""
    h2b = _HASH_TO_BOOKS
    matches: dict[int, int] = {}
    table = pq.read_table(parquet_path, columns=['text'])
    for text in table.column('text').to_pylist():
        if not text:
            continue
        words = text.lower().split()
        for i in range(len(words) - NGRAM_SIZE + 1):
            h = xxhash.xxh64(' '.join(words[i:i + NGRAM_SIZE])).intdigest()
            idxs = h2b.get(h)
            if idxs is not None:
                for idx in idxs:
                    matches[idx] = matches.get(idx, 0) + 1
    return matches


def _collect_parquet_paths(parquets_file: Path, raw_dir: Path) -> list[str]:
    """Read parquet filenames from a txt file (one per line) and resolve against raw_dir."""
    paths = []
    with open(parquets_file) as f:
        for line in f:
            rel = line.strip()
            if rel:
                paths.append(str(raw_dir / rel))
    return paths


def write_contamination_stats(excerpts: list[dict], matching: dict[int, int], stats_dir: Path):
    stats_dir.mkdir(parents=True, exist_ok=True)
    n_total = len(excerpts)
    n_contaminated = sum(1 for i in range(n_total) if i in matching)

    path = stats_dir / 'fineweb_containment_stats.txt'
    with open(path, 'w') as f:
        f.write(f"n={n_total}\n")
        f.write(f"contaminated={n_contaminated} ({n_contaminated / n_total * 100:.1f}%)\n")
        f.write(f"clean={n_total - n_contaminated} ({(n_total - n_contaminated) / n_total * 100:.1f}%)\n")
        counts = np.array([matching[i] for i in range(n_total) if i in matching])
        if counts.size:
            f.write(
                f"matching_ngrams (contaminated only): "
                f"min={counts.min()}  median={int(np.median(counts))}  max={counts.max()}\n"
            )
    print(f"Containment stats -> {path}")


def write_containment_scatter(excerpts: list[dict], matching: dict[int, int], stats_dir: Path):
    """Perplexity vs Min-K%++ colored by FineWeb containment. Skipped if scores absent."""
    import matplotlib.pyplot as plt

    scored = [
        (r, i in matching)
        for i, r in enumerate(excerpts)
        if r.get('perplexity') is not None and r.get('min_k_pp') is not None
    ]
    if not scored:
        print("No perplexity/min_k_pp scores found — skipping containment scatter.")
        return

    log_ppl = np.log10([r['perplexity'] for r, _ in scored])
    min_k_pp_vals = np.array([r['min_k_pp'] for r, _ in scored])
    in_fw = np.array([flag for _, flag in scored])

    stats_dir.mkdir(parents=True, exist_ok=True)
    fig, ax = plt.subplots(figsize=(8, 5))

    for label, mask, color, marker in [
        ('in FineWeb', in_fw, 'red', 'x'),
        ('not in FineWeb', ~in_fw, 'steelblue', 'o'),
    ]:
        ax.scatter(
            log_ppl[mask], min_k_pp_vals[mask],
            c=color, marker=marker, s=8, alpha=0.5, linewidths=0.5,
            label=f"{label} (n={mask.sum():,})",
        )

    ax.set_xlabel("Perplexity (log₁₀ scale)")
    ax.set_ylabel("Min-K%++ z-score")
    ax.set_title(f"Perplexity vs Min-K%++  n={len(scored):,}  colored by FineWeb-Edu containment")
    ax.xaxis.set_major_formatter(plt.FuncFormatter(lambda x, _: f"{10 ** x:.0f}"))
    ax.legend(fontsize=8)
    fig.tight_layout()

    path = stats_dir / 'containment_ppl_min_k_pp.png'
    fig.savefig(path, dpi=120)
    plt.close(fig)
    print(f"Containment scatter -> {path}")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--sampled', required=True,
                        help='sampled.jsonl with text_excerpt field')
    parser.add_argument('--parquets-file', required=True,
                        help='txt file with one parquet filename per line (from parse_tokenized_parquets.py)')
    parser.add_argument('--raw-dir', required=True,
                        help='root directory of raw FineWeb-Edu parquet files')
    parser.add_argument('--output', required=True)
    parser.add_argument('--stats-dir', default=None)
    parser.add_argument('--workers', type=int, default=96)
    parser.add_argument('--max-parquets', type=int, default=None,
                        help='Scan only the first N parquets (for timing tests)')
    args = parser.parse_args()

    # Load excerpts
    excerpts = []
    with open(args.sampled) as f:
        for line in f:
            if line.strip():
                excerpts.append(json.loads(line))
    print(f"Loaded {len(excerpts)} excerpts")

    # Build query: hash → list of excerpt indices
    global _HASH_TO_BOOKS
    for idx, r in enumerate(excerpts):
        for h in _ngram_hashes(r['text_excerpt']):
            _HASH_TO_BOOKS.setdefault(h, []).append(idx)
    print(f"Query: {len(_HASH_TO_BOOKS):,} unique 13-gram hashes from {len(excerpts)} excerpts")

    # Collect parquet paths from the pre-parsed list
    parquet_paths = _collect_parquet_paths(
        Path(args.parquets_file), Path(args.raw_dir)
    )
    if args.max_parquets is not None:
        parquet_paths = parquet_paths[:args.max_parquets]
        print(f"[test mode] limited to {len(parquet_paths)} parquets")
    print(f"Scanning {len(parquet_paths)} parquet files with {args.workers} workers")

    # Scan parquets in parallel; workers inherit _HASH_TO_BOOKS via fork
    matching: dict[int, int] = {}
    t0 = time.time()
    print_every = max(1, len(parquet_paths) // 10)
    with Pool(args.workers) as pool:
        for done, result in enumerate(
            pool.imap_unordered(_scan_parquet, parquet_paths, chunksize=1), 1
        ):
            for idx, count in result.items():
                matching[idx] = matching.get(idx, 0) + count
            if done % print_every == 0 or done == len(parquet_paths):
                elapsed = time.time() - t0
                rate = done / elapsed
                eta = (len(parquet_paths) - done) / rate if rate > 0 else 0
                print(f"  {done}/{len(parquet_paths)} parquets  {len(matching)} contaminated"
                      f"  {elapsed:.0f}s elapsed  ~{eta:.0f}s remaining"
                      f"  ({rate:.2f} parquets/s)")

    # Write output
    out_path = Path(args.output)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, 'w') as f:
        for idx, r in enumerate(excerpts):
            r['in_fineweb'] = idx in matching
            r['fineweb_matching_ngrams'] = matching.get(idx, 0)
            f.write(json.dumps(r) + '\n')

    n_contaminated = len(matching)
    print(f"\nContaminated: {n_contaminated}/{len(excerpts)} ({n_contaminated / len(excerpts) * 100:.1f}%)")
    print(f"Output: {out_path}")

    if args.stats_dir:
        stats_dir = Path(args.stats_dir)
        write_contamination_stats(excerpts, matching, stats_dir)
        write_containment_scatter(excerpts, matching, stats_dir)


if __name__ == '__main__':
    main()